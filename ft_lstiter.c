#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	while (lst != NULL)
	{
		(*f)(lst->content);
		lst = lst->next;
	}
}
// fungsi ini berguna untuk mengaplikasikan
// fungsi yang diinginkan ke semua node yang ada