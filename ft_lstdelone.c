#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void*))
{
	if (lst == NULL || del == NULL)
		return ;
	(*del)(lst->content);
	free(lst);
}
// fungsi ini buat menghapus node bserta isiannya
// the func that delete the content
// replaced by 'd' to proof that it is deleted